# program for the kilometers_to_miles.py
# converts kilometers to miles
# Take kilometers and convert to miles

# taking input from the user in float for kilometers

kilometers = float(input("Enter the kilometers: "))

# converion Factor for kilometers to miles
converion_factor = 0.621371

# calculating miles
miles = kilometers * converion_factor

# print the kilometers and miles
print("The kilometers is: ", kilometers)
print("The miles is: ", miles)

